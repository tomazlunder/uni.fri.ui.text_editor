    private void clearSelection() {
        StyleContext context = new StyleContext();
        Style style = context.addStyle("mystyle", null);
        StyleConstants.setBackground(style, Color.WHITE);
        StyleConstants.setForeground(style, Color.BLACK);
        for (int i = 0; i < selected.size(); i += 2) {
            jTextPane2.select(selected.get(i), selected.get(i + 1));
            jTextPane2.setCharacterAttributes(style, true);
        }

        selected = new ArrayList<>();
        jTextPane2.setEditable(true);

    }

    private void jMenuItemNajdiAP(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemNajdiAP

        clearSelection();

        String isci = jTextField1.getText();
        String text = jTextPane2.getText();

        if (isci.length() == 0) {
            return;
        }

        int pojavitev = 0;
        int add = 0;

        Scanner sc = new Scanner(text);
        while(sc.hasNextLine()){
            int index = 0;
            String line = sc.nextLine();
            while (true) {
                int found = line.indexOf(isci, index);
                if (found != -1) {
                    pojavitev++;
                    jTextPane2.select(found + add, found + isci.length() + add);
                    index = found + isci.length();
                    barvajIzbrano(Color.YELLOW, true);
                    selected.add(found + add);
                    selected.add(found + isci.length() + add);
                    
                }
                if (found == -1) {
                    break;
                }
            }
            add+=line.length()+1;
        }
        
        jLabel3.setText("Najdenih " + pojavitev + " pojavitev");
        if(pojavitev > 0){
            jTextPane2.grabFocus();
            jTextPane2.moveCaretPosition(selected.get(0));
            jTextPane2.select(selected.get(0), selected.get(0));
            
            jTextPane2.setEditable(false);
            jTextPane2.getCaret().setVisible(true);
            
            jLabel1.setText(OBVESTILA + "Klikni gumb X za nadaljevanje urejanja.");
            
        }
        
        //if(pojavitev > 0) jTextPane2.setEditable(false);

        //jTextPane2.grabFocus();
        //jTextPane2.moveCaretPosition(i);
    }//GEN-LAST:event_jMenuItemNajdiAP

