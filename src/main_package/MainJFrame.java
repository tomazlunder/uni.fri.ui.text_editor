    private void jMenuItemPapajščinaAP(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemPapajščinaAP
        String besedilo = jTextPane2.getText();
        //System.out.println("Pap");

        String samoglasniki = "AEIOUaeiou"; 
        int dol_besedila = besedilo.length(); 
        //System.out.printf("%d\n",dol_besedila);
        int i = 0;
        int st_samo = 0; 
        char prejsnji = 'a';
        String besedilo2 = ""; // 
        while (i < dol_besedila) { // pregledamo vse znake
            //System.out.printf("%s\n",besedilo2);
            char znak = besedilo.charAt(i); // i-ti znak
            int code = (int) znak;
            if(code == 32 || code == 9 || code == 10 || code == 11 || code == 12 || code == 13){
                prejsnji = 'a';
                besedilo2 = besedilo2 + znak;
                i = i + 1;
                continue;
            }
            besedilo2 = besedilo2 + znak;// nadaljujemo novi niz iz prebranih znakov
            if (samoglasniki.indexOf(znak) != -1){
                besedilo2 = besedilo2 + "p" + prejsnji;
                prejsnji = znak;
                st_samo = st_samo + 1;
            }
            i = i + 1;
        }
        
        jTextPane2.setText(besedilo2);
        
        jLabel1.setText(OBVESTILA + "Uspešno prevedeno v Papajščino.");
    }//GEN-LAST:event_jMenuItemPapajščinaAP

