    private void barvajIzbrano(Color c, boolean bg) {
        StyleContext context = new StyleContext();
        Style style = context.addStyle("mystyle", null);
        if (bg) {
            StyleConstants.setBackground(style, c);
        } else {
            StyleConstants.setForeground(style, c);
        }

        jTextPane2.setCharacterAttributes(style, true);
    }

